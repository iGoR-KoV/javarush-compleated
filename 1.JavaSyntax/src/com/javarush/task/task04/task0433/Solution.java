package com.javarush.task.task04.task0433;

/* 
Гадание на долларовый счет
*/

public class Solution {
    public static void main(String[] args) {
        int n = 0;
        while (n != 10) {
            int s = 0;
            while (s != 10) {

                System.out.print("S");
                s++;
            }
            System.out.println("S");
            n++;
        }

    }
}
